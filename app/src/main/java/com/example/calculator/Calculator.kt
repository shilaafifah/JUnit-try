package com.example.calculator

class Calculator {

    fun add(a: Int, b: Int): Int {
        // Fungsi untuk menerima 2 parameter bertipe Int
        return a + b // Mengembalikan hasil penjumlahan dari kedua bilangan tersebut
    }

    fun subtract(a: Int, b: Int): Int {
        // Fungsi untuk menerima dua parameter bertipe Int
        return a - b // Mengembalikan hasil pengurangan dari kedua bilangan tersebut
    }
}